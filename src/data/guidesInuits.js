export function getGuideById(id) {
  return GUIDES_INUITS.find(g => g.id === id)
}

export function getGuideByNumber(number) {
  return GUIDES_INUITS.find(g => g.number === number)
}

export function getUnlockedGuides(currentStep) {
  return GUIDES_INUITS.filter(g => g.number <= currentStep)
}
